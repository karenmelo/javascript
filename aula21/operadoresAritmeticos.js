/**
 *  +  adição / concatenação
 *  -  subtração
 *  /  divisão
 *  *  multiplicação
 *  ** potenciação
 *  %  resto da divisão
 *  ++ incremento
 *  -- decremento
 * 
 * 
 *  Precedencia de operações
 *   ()
 *   **
 *   *  /   %
 *   +  -
 * 
 * 
 *  Operadores de atribuição
 *  +=
 *  -=
 *  *=
 *  **=
 *  
 * 
 *  Conversão de tipos
 *  parseInt()
 *  parseFloat()
 *  Number('2')
 */

const num1 = 2;
const num2 = 10;
console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 / num2);
console.log(num1 * num2);
console.log(num1 ** num2);
console.log(num1 % num2);